    // Main initialization
    async initialize() {
      if (this.initialized) return;

      console.log('üè• Initializing ILL Workflow System...');
      
      try {
        await this.setupUI();
        await this.setupEventListeners();
        await this.restoreActiveWorkflows();
        
        this.initialized = true;
        console.log('‚úÖ ILL Workflow System initialized');
        
        return { status: 'success', module: 'ILLWorkflow' };
      } catch (error) {
        console.error('‚ùå Failed to initialize ILL Workflow:', error);
        return { status: 'error', module: 'ILLWorkflow', error: error.message };
      }
    }

